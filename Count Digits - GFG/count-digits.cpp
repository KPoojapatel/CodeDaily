class Solution{
public:
    int evenlyDivides(int N){
        int count=0;
        int originalN = N;
        while(N>0){
            int remender=N%10;
            if(remender!=0&&originalN%remender==0)
            {
                count++;
            }
            N /= 10;
        }
        return count;
    }
};

